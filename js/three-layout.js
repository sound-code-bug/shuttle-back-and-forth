function makeElement(i) {
  const el = document.createElement("div");
  el.className = "box";
  el.style.width = BOX_W + "px";
  el.style.height = BOX_H + "px";
  el.innerHTML = `
    <div class="inner">
      <div class="tool">
        <span class="btn close"></span>
        <span class="btn minimize"></span>
        <span class="btn maximize"></span>
      </div>
      <div class="content">${loveMessages[i % loveMessages.length]}</div>
    </div>
  `;
  return el;
}

function makeBox(i) {
  const el = makeElement(i);
  const obj = new CSS3DObject(el);
  const rangeX = vw;
  const rangeY = vh;
  obj.position.x = (Math.random() - 0.5) * rangeX;
  obj.position.y = (Math.random() - 0.5) * rangeY;
  obj.position.z = -Math.random() * 2400 - 200;
  obj.rotation.z = (Math.random() - 0.5) * 0.25;
  obj._inner = el.querySelector(".inner");
  return obj;
}

function setup() {
  const container = document.querySelector(".container");
  if (!container) return;

  const r = new CSS3DRenderer();
  vw = window.innerWidth;
  vh = window.innerHeight;
  r.setSize(vw, vh);
  r.domElement.style.position = "absolute";
  r.domElement.style.top = "0";
  r.domElement.style.left = "0";
  container.innerHTML = "";
  container.appendChild(r.domElement);
  renderer = r;

  scene = new THREE.Scene();
  camera = new THREE.PerspectiveCamera(
    60,
    window.innerWidth / window.innerHeight,
    1,
    5000
  );
  camera.position.z = 800;

  isMobile =
    window.matchMedia("(max-width: 768px)").matches ||
    /Mobi|Android/i.test(navigator.userAgent);
  MAX_OBJECTS = isMobile ? 60 : 120;
  const interval = isMobile ? 350 : 240;
  spawnTimer = setInterval(() => {
    if (objects.length >= MAX_OBJECTS) {
      clearInterval(spawnTimer);
      spawnTimer = null;
      return;
    }
    const batch = Math.min(
      MAX_OBJECTS - objects.length,
      3 + Math.floor(Math.random() * 3)
    );
    for (let i = 0; i < batch; i++) {
      const o = makeBox(msgIndex++);
      objects.push(o);
      scene.add(o);
    }
  }, interval);

  if (!viewer) {
    viewer = document.createElement("div");
    viewer.className = "viewer";
    viewer.innerHTML =
      '<div class="panel"><span class="close">×</span><div class="text"></div></div>';
    document.body.appendChild(viewer);
    const close = viewer.querySelector(".close");
    close.addEventListener("click", () => hideViewer());
  }
  renderer.domElement.addEventListener(
    "click",
    (e) => {
      const inner = e.target.closest(".inner");
      if (!inner) return;
      const textEl = inner.querySelector(".content");
      const txt = textEl ? textEl.textContent : "";
      showViewer(txt);
    },
    { passive: true }
  );

  animate();
}

let lastTime = performance.now();
function animate(now = performance.now()) {
  requestAnimationFrame(animate);
  const delta = Math.min(0.05, (now - lastTime) / 1000);
  lastTime = now;
  const speed = isMobile ? 4 : 6;
  const near = camera.position.z;
  const denom = near + 2400;
  for (let i = 0; i < objects.length; i++) {
    const o = objects[i];
    o.position.z += speed * (delta * 60);
    let t = (o.position.z + 2400) / denom;
    if (t < 0) t = 0;
    if (t > 1) t = 1;
    const ax = Math.min(1, Math.abs(o.position.x) / (vw * 0.25));
    const ay = Math.min(1, Math.abs(o.position.y) / (vh * 0.22));
    const centerFactor = Math.max(ax, ay);
    const avoidX = vw * 0.18;
    const avoidY = vh * 0.16;
    if (Math.abs(o.position.x) < avoidX && Math.abs(o.position.y) < avoidY) {
      const tx = (o.position.x >= 0 ? 1 : -1) * avoidX;
      const ty = (o.position.y >= 0 ? 1 : -1) * avoidY;
      o.position.x += (tx - o.position.x) * 0.08;
      o.position.y += (ty - o.position.y) * 0.06;
    }
    const inCenter =
      Math.abs(o.position.x) < avoidX && Math.abs(o.position.y) < avoidY;
    const scaleBase = 0.7 + 0.45 * t;
    const scale =
      scaleBase * (0.85 + 0.15 * centerFactor) * (inCenter ? 0.8 : 1);
    const opacityBase = 0.25 + 0.75 * t;
    const opacity = Math.max(
      0.25,
      Math.min(
        0.95,
        opacityBase * (0.6 + 0.4 * centerFactor) * (inCenter ? 0.7 : 1)
      )
    );
    const inner = o._inner;
    if (inner) {
      inner.style.transform = `scale(${scale})`;
      inner.style.opacity = String(opacity);
    }
    if (o.position.z > camera.position.z + 120) {
      scene.remove(o);
      objects.splice(i, 1);
      i--;
      const n = makeBox(msgIndex++);
      objects.push(n);
      scene.add(n);
      continue;
    }
  }
  renderer.render(scene, camera);
}

function onResize() {
  if (!renderer) return;
  vw = window.innerWidth;
  vh = window.innerHeight;
  renderer.setSize(vw, vh);
  camera.aspect = vw / vh;
  camera.updateProjectionMatrix();
  isMobile =
    window.matchMedia("(max-width: 768px)").matches ||
    /Mobi|Android/i.test(navigator.userAgent);
  MAX_OBJECTS = isMobile ? 60 : 120;
}

function showViewer(txt) {
  const t = viewer.querySelector(".text");
  t.textContent = txt || "";
  viewer.classList.add("show");
  if (autoCloseTimer) clearTimeout(autoCloseTimer);
  autoCloseTimer = setTimeout(() => hideViewer(), 5000);
}

function hideViewer() {
  viewer.classList.remove("show");
}

